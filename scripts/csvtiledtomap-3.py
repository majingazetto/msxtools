def datatodb (data):

    db = ""

    for i in range (0, len(data)):
        if ((i & 7) == 0):
            db += "\t\tDEFB\t" 
        byte = data[i]
        db += "#"
        if (byte < 0x10):
            db += "0"
        db += "%X" % byte
        if (((i & 7) != 7) & (i != len(data) -1)):
            db += ", "
        else:
            db += "\n"

    db += "\n\n"
    return db


def screenstodb (screens, basename, mapsizex, mapsizey, screendata, screensizex, screensizey, compress = False):

    basepad = basename.upper().ljust(8, " ")
    db = ""

    # Title
    db += ";                                       ****************************************\n"
    db += ";                                       * ----- AUTOGENERATED MAP DATA ------- *\n"
    db += ";                                       *               " + basepad + "               *\n"
    db += ";                                       ****************************************\n"
    db += "\n"
    
    db += basename 
    
    # Map data
    sizex = mapsizex / screensizex
    sizey = mapsizey / screensizey


    db += "\t\tDEFB\t"
    db += bytetohexstr(sizex) + ", "
    db += bytetohexstr(sizey)
    db += "\n\n"
    

    # Generate Pointers

    pointers = getmappointers (screens, mapsizex, mapsizey, screendata, screensizex, screensizey)

    db += "\n"
    db += ";\n"
    db += ";\t\t--- SCREEN TABLE POINTERS ---\n"
    db += ";\n"
    db += "\n\n"
    db += ".SCRPT" 

    for y in range (0, sizey):
        db += "\t\tDEFW\t"
        for x in range (0, sizex):
            scr = pointers[x][y] 
            mx = scr[0]
            my = scr[1]

            if (scr[2] == []):
                db += "#0000" 
            else:
                db += ".SP" + str(mx) + str(my)
            if (x < (sizex - 1)):
                db += ", "
        db += "\n"
    db += ".SCRPTE\n"

    # Generate Screen Data

    db += "\n\n"
    db += ";\n"
    db += ";\t\t--- SCREEN DATA ---\n"
    db += ";\n"
    db += "\n\n"

    for row in pointers:
        for scr in row:
            data = scr[2]
            if (data != []):
                mx = scr[0]
                my = scr[1]
                basename = ".SP" + str(mx) + str(my) 
                db += "; ------------------------\n"
                db += "; " + basename + "\n"
                db += "; ------------------------\n\n"
                db += basename

                # screen data
                wx = data[4]
                wy = data[5]

                db += "\t\tDEFB\t" + bytetohexstr(wx) + ", " + bytetohexstr(wy)
                db += "\n"
                db += "\t\tDEFW\t" + basename + "MAP" + "\n"

                # extra pointers

                db += "\n\n"

                # map data 
                if (compress): 
                    sdata = data[1]
                else:
                    sdata = data[0]

                db += "\t\t; MAP DATA"
                if (compress):
                    db += " (PACKED)"
                db += "\n\n"
                db += basename + "MAP\n"
                db += datatodb(sdata)
                db += "\n\n"

    return  db

