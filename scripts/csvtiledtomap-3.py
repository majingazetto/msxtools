def extractscreens (mapdata, mapsizex, mapsizey, splitdata, sizex, sizey, compress = False):

    pos = 0
    x = 0
    y = 0
    screens = []
    while (y < mapsizey):
        if (splitdata[pos] == 0):
            scr, tr, wx, wy = splitscreen(mapdata, mapsizex, mapsizey, splitdata, sizex, sizey, pos)
            cscr = [] 
            if (compress):
                cscr = compressdata(scr)
            screens.append((scr, cscr, x, y, wx, wy)) 
            x = tr % mapsizex
        x = x + 1
        if (x >= mapsizex):
            x = 0
            y = y + 1
        pos = (y * mapsizex) + x
         
    return screens


def splitscreen (mapdata, mapsizex, mapsizey, splitdata, sizex, sizey, pos):
    
    screen = []
    
    tl, tr, bl, br = getscrpositions (mapsizex, mapsizey, splitdata, sizex, sizey, pos)
    wx = (tr - tl) + 1
    wy = ((bl / mapsizex) - (tl / mapsizex)) + 1
    screen = getwindowfromarray(mapdata, wx, wy, tl, mapsizex)

    return screen, tr, wx, wy


def getscrpositions (mapsizex, mapsizey, splitdata, sizex, sizey, pos):

    topleft = pos
    topright = pos
    bottomleft = pos
    bottomright = pos
    
    idx = 0

    # Get top right
    while (idx < mapsizex):
        topright = idx + pos
        if (splitdata[topright] == 1):
            break
        idx = idx + 1

    # Get bottom left 
    idx = 0
    while (idx < mapsizey):
        bottomleft = (idx * mapsizex) + pos
        if (splitdata[bottomleft] == 2):
            break
        idx = idx + 1 

    # Get bottom right
    bottomright = bottomleft + (topright - pos)
    
    return topleft, topright, bottomleft, bottomright


def getmappointers (screens, mapsizex, mapsizey, screendata, screensizex, screensizey):
    
    pointers = []
    sizex = mapsizex / screensizex
    sizey = mapsizey / screensizey

    # Init
    for y in range (0, sizey):
        row = []
        pointers.append(row)
        for x in range (0, sizex):
            scr = [x, y, []]
            row.append(scr)

    for scr in screens:
        # get x, y pointer
        x = scr[2] 
        y = scr[3]
        mx = x / screensizex
        my = y / screensizey

        pointers[mx][my][2] = scr

    return pointers

