def compressdata (data):
    
    cdata = []

    # data to tmp
    writefile (TMPFILE, data)
    
    # compress data with packer
    subprocess.call([PACK, TMPFILE])

    # read file
    cdata = readfile(PACKTMPFILE)

    # delete temporal files
    os.remove(TMPFILE) 
    os.remove(PACKTMPFILE) 

    return cdata




