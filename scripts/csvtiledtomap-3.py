def str2bool (boolstr):
    
    if boolstr.lower() in ("yes", "true", "t", "y", "1"):
        return True
    
    return False


def bytetohexstr (byte):
    db = "#"
    if (byte < 0x10):
        db += "0"
    db += "%X" % byte
    return db

