def toplatform (layer, w, h, sx, sy, outputname):
    table = []
    pnt = 0
    x = 0
    y = 0

    while (pnt < len(layer)):
        if (layer[pnt] != 0):
            platform = []
            idx = pnt 
            while (pnt < len(layer) and layer[pnt] != 0):
                platform.append(layer[pnt] - 1)
                pnt = pnt + 1
                x = x + 1
                if (x >= sx):
                    x = 0
                    y = y + 1
                
            table.append({
                    'pnt': idx,
                    'size': len(platform),
                    'data': platform
                })
        else:
            pnt = pnt + 1
            x = x + 1
            if (x >= sx):
                x = 0
                y = y + 1
            
    db = ""

    # Create source file
    # and binary file

    label = os.path.splitext(os.path.basename(outputname))[0]
    
    binary = []
    db = ""
    db += "; - " + label + "\n" 
    db += "; - Autogenerated\n"
    db += "; - WORD PNT \n"
    db += "; - WORD SIZE \n"
    db += "; - BYTE[] PLATFORM \n"
    db += "; -\n"
    db += "\n" + label + "\n\n" 

    for entry in table:
        pnt = entry['pnt']
        size = entry['size']
        data = entry['data']

        # Pointer

        word = "{0:#0{1}x}".format(pnt , 6)[2:]
        word = "#" + word.upper()
        db += "\t\tDEFW\t"
        db += word
        db += "\n"

        binary.append(pnt & 0xFF)
        binary.append(pnt >> 8)



        # Size

        word = "{0:#0{1}x}".format(size , 6)[2:]
        word = "#" + word.upper()
        db += "\t\tDEFW\t"
        db += word
        db += "\n"

        binary.append(size & 0xFF)
        binary.append(size >> 8)

        # Byte array
        
        for i in range (0, len(data)):
            if ((i & 7) == 0):
                db += "\t\tDEFB\t" 
            byte = data[i]
            binary.append(byte & 0xFF)
            db += "#"
            if (byte < 0x10):
                db += "0"
            db += "%X" % byte

            if ((i & 7) != 7):
                if (i < len(data) - 1):
                    db += ", "
            else:
                db += "\n"

        db += "\n\n"

    # End

    word = "{0:#0{1}x}".format(0xFFFF , 6)[2:]
    word = "#" + word.upper()
    db += "\t\tDEFW\t"
    db += word
    db += "\n"
    binary.append(0xFF)
    binary.append(0xFF)


    db += ".END"
    db += "\n\n"


    writestringfile (outputname, db)

    # Generate .BIN file (binary)
    writefile(outputname + ".BIN", binary)



