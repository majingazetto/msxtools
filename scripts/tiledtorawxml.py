def main(args):
    
    logo()
   
    if (len(args) < 5):
        usage(args[0])
        exit (-1)

    filename = args[1]
    layername = args[2]
    mode = args[3]
    outputname = args[4]
    sx = 0
    sy = 0
    print("Input:  ", filename)
    print("Layer:  ", layername)
    print("Mode:   ", mode)
    print("Output: ", outputname)

    # Read XML File

    xmldata = ET.parse(filename)
    root = xmldata.getroot()

    # Get SX and SY (for table mode)
    
    w = int(root.attrib[W_PROPERTY])
    h = int(root.attrib[H_PROPERTY])

    # Get main SX and SY
    print ("Width:  ", w)
    print ("Height: ", h)

    layer = None
    for child in root.iter(LAYER_CHILD):
        if ((child.get(NAME_PROPERTY, None)) == layername):
            layer = child
                        
    if layer is None:
        print ("ERROR: Layer >%s< not found" % (layername))
        exit(-1)

    # Get local (layer) SX and SY 
    if (layer.attrib[W_PROPERTY]):
        sx = int(layer.attrib[W_PROPERTY])
        print ("Local Width: ", sx)
    if (layer.attrib[H_PROPERTY]):
        sy = int(layer.attrib[H_PROPERTY])
        print ("Local Height: ", sy)

    # get data

    data = []
    edata = layer.find(LAYER_DATA_CHILD)
    if (edata is None): 
        print ("ERROR: Data for %s not found!!")
        exit (-1)
    else:
        tdata = edata.text
        if (tdata is None):
            print ("ERROR: Data for %s not found!!")
            exit (-1)
        else:
            datastr = tdata.replace("\n","").split(",")
            for e in datastr:
                data.append(int(e))

    func = mainswitcher(mode) 
    func(data, w, h, sx, sy, outputname) 


    exit(0)

def mainswitcher(case): 
    switcher = {
            'raw': toraw,
            'table': totable,
            'rawtable': torawtable,
            'platform': toplatform
    }

    func = switcher.get(case, lambda: 'nothing')
    return func




# ---------------------
# Call to Main
# ---------------------

main(sys.argv)    



