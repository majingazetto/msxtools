def readlayerobject (jsondata, name):

    layers = jsondata["layers"]
    for l in layers:
        if (l["name"] == name):
            return l            
            break

    return {}


def datatodb (data):

    db = ""

    db += "; Autogenerated  \n\n\n"

    for i in range (0, len(data)):
        if ((i & 7) == 0):
            db += "\tDEFB\t" 
        byte = data[i]
        db += "#"
        if (byte < 0x10):
            db += "0"
        db += "%X" % byte
        if ((i & 7) != 7):
            db += ", "
        else:
            db += "\n"

    db += "\n\n"
    return db


