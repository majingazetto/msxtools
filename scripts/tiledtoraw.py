def toraw (layer, w, h, sx, sy, outputname):


    # pattern - 1
    for i in range(0, len(layer)):
        pat = layer[i]
        if (pat != 0):
            pat = pat - 1
            layer[i] = pat

    db = datatodb (layer)

    # Write file
    writefile (outputname, layer)
    writestringfile (outputname + ".Z8A", db)


