def totable (layer, w, h, sx, sy, outputname):

    numscrs = (w / sx) * (h / sy)
    table = []
    scr = 0
    cx = 0
    cy = 0
    xscr = 0
    yscr = 0
    scr = 0
    cnt = 0
    x = 0
    y = 0
    for i in range(0, len(layer)):
        pat = layer[i]
        if (pat != 0): 
            pat = pat - 1
            table.append({
                'idscr': scr,
                'x': x,
                'y': y,
                'xr': xscr,
                'yr': yscr,
                'pat': pat
                })
        
        xscr = xscr + 1
        if (xscr >= sx):
            xscr = 0
        cx = (cx + 1)
        x = x + 1
        if (cx >= w):
            cx = 0
            x = 0
            y = y + 1
            yscr = yscr + 1
 
        if (yscr >= sy):
            cy += (w / sx)
            yscr = 0

        scr = cy + (cx / sx) 

    # SPECIAL ORDER AND DATA FOR NEST

    def byidscr (element):
        return element["idscr"]
   
    # Soft by idscr
    tsorted = sorted(table, key=byidscr)

    # offsets to idscr, counters, and prepared data

    offsets = []
    
    # fill offsets
    for n in range (0, numscrs):
        offsets.append((0,0))

    data = []
    
    # Prepare data
    cnt = 0
    off = 0
    oldid = 0 
    for element in tsorted:
        offsets[oldid] = (cnt, off)

        if (oldid != element["idscr"]):
            oldid = element["idscr"]
            off = off + (cnt << 1)
            cnt = 0

        xy = (((element["xr"] & 0x0F) ) |  ((element["yr"] & 0xF) <<4)) & 0xFF
        data.append(xy)
        data.append(element["pat"])
        cnt = cnt + 1 
        offsets[oldid] = (cnt, off)
    
    # Create source file
    # and binary file

    label = os.path.splitext(os.path.basename(outputname))[0]
    
    binary = []
    db = ""
    db += "; Autogenerated  \n\n\n"
    db += label + "\n\n" 

    db += ".OFFSET" 
    

    for off in offsets:

        word = "{0:#0{1}x}".format(off[0] , 4)[2:]
        word = "#" + word.upper()
        db += "\t\tDEFB\t"
        db += word
        db += "\n"


        word = "{0:#0{1}x}".format(off[1] , 6)[2:]
        word = "#" + word.upper()
        db += "\t\tDEFW\t"
        db += word
        db += "\n"

        binary.append(off[0])
        binary.append(off[1] & 0xFF)
        binary.append(off[1] >> 8)


    db += ".DATA" 
    
    for i in range (0, len(data)):
        if ((i & 7) == 0):
            db += "\t\tDEFB\t" 
        byte = data[i]
        binary.append(byte & 0xFF)
        db += "#"
        if (byte < 0x10):
            db += "0"
        db += "%X" % byte

        if ((i & 7) != 7):
            if (i < len(data) - 1):
                db += ", "
        else:
            db += "\n"

    db += "\n\n"
    db += ".END"
    db += "\n\n"

    writestringfile (outputname, db)

    # Generate .BIN file (binary)
    writefile(outputname + ".BIN", binary)


