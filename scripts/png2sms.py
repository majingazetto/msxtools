def pixelstoraw (pixels):
    raw = []
    for row in pixels:
        for i in range(0, len(row)):
            raw.append(row[i])
    return raw

def getpaletteindexes (pixels, pal):

    palindexes = []

    for pixel in pixels:
        if (pixel in palindexes) == False:
            palindexes.append(pixel)

        if (len(palindexes) == 16):
            break

    palindexes = sorted(palindexes)
    return palindexes


