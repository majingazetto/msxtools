def getrawpat (pat, rawdata):
    patraw = []
    y = ((pat & 0xE0) >> 5) * 256 * 8
    x = (pat & 0x1F) * 8
    offset = x + y
    for line in range (0, 8):
        for pixel in range(0, 8):
            index = (offset + (line * 256)) + pixel
            colour = rawdata[index]
            if (colour > 15):
                colour = 0
            patraw.append(colour)

    return patraw

def patrawtomode4 (patraw, palindex):

    patmode4 = []

    for line in range (0, 8):
        by0 = 0
        by1 = 0
        by2 = 0
        by3 = 0
        for pixel in range (0, 8):

            rawbyte = patraw[(line * 8) + pixel]
            byte = palindex.index(rawbyte) 
            
            b0 = byte & 1
            b1 = (byte >> 1) & 1
            b2 = (byte >> 2) & 1
            b3 = (byte >> 3) & 1
            by0 = by0 << 1
            by1 = by1 << 1
            by2 = by2 << 1
            by3 = by3 << 1
            by0 = (by0 | b0) 
            by1 = (by1 | b1)
            by2 = (by2 | b2)
            by3 = (by3 | b3)
        
        patmode4.append(by0)
        patmode4.append(by1)
        patmode4.append(by2)
        patmode4.append(by3)

    return patmode4

def rawtomode4 (rawdata, palindex):
    mode4 = []
    for pat in range(0, 256):
        patraw = getrawpat(pat, rawdata)
        patmode4 = patrawtomode4(patraw, palindex)
        for line in patmode4:
            mode4.append(line)
    return mode4


def color2sms (color):

    smscolor = 0x00;
    if (color == 0x55):
        smscolor = 0x01;
    if (color == 0xAA):
        smscolor = 0x02;
    if (color == 0xFF):
        smscolor = 0x03;
    return smscolor;




def paltomode4 (palette, palindex):

    pal = [];

    for idx in palindex:
        colour = palette[idx]

        r = colour[0];
        g = colour[1];
        b = colour[2];


        #r = ((r / 255.0) * 3.0)
        #g = ((g / 255.0) * 3.0)
        #b = ((b / 255.0) * 3.0)
        #r = int(r)
        #g = int(g)
        #b = int(b)

        r = color2sms(r);
        g = color2sms(g);
        b = color2sms(b);
        rgb = b << 4 | g << 2 | r

        pal.append (rgb)
        
   
    if len(pal) < 16:
        add = 16 - len(pal)
        for i in range(add):
            pal.append(0x00)

    return pal


def palmode4todb (pal):

    paldb = ""
    paldb += "; Autogenerated by PNG2SMS Tool \n\n\n"
    paldb += "\tDEFB\t" 

    for i in range (0, 8):
        col = pal[i]
        paldb += "#"
        if (col < 0x10):
            paldb += "0"
        paldb += "%X" % col
        if (i < 7):
            paldb += ", "

    paldb += "\n\tDEFB\t" 
    for i in range (8, 16):
        col = pal[i]
        paldb += "#"
        if (col < 0x10):
            paldb += "0"
        paldb += "%X" % col
        if (i < 15):
            paldb += ", "

    paldb += "\n\n"

    return paldb


