static unsigned char g_pCharset[2048];

static char g_pszPrintBuffer [MAX_LEN];

static int g_wX     = 0;
static int g_wY     = 0;
int g_wColor = 0xffffff;
static int g_wPrintMode = PRINTMODE_SHADOW;

// ---------------------------------------------------------------------------

void Locate ( int wX, int wY)
{
    g_wX = wX;
    g_wY = wY;
}

// ---------------------------------------------------------------------------

void Ink ( int wColor)
{
    g_wColor = wColor;
}

// ---------------------------------------------------------------------------
  
void Print ( char * pszFormat, ...)
{

    va_list vl;
    va_start(vl,pszFormat);

    vsnprintf ( g_pszPrintBuffer, MAX_LEN, pszFormat, vl);

    g_pszPrintBuffer [MAX_LEN - 1] = 0;
    PrintAt ( g_pszPrintBuffer, g_wX, g_wY, g_wColor);
    va_end(vl);
    

}

// ---------------------------------------------------------------------------

void SetPrintMode ( int wMode)
{
    if ( wMode < PRINTMODE_INVALID)
        g_wPrintMode = wMode;
}

