#define PIXEL(n)   if ( nPix8 < 0) pChunk [ n] = wColor; nPix8 = nPix8 << 1;

void DrawChar16 ( unsigned char ubChar, short *pChunk, int wModulo, int wColor)
{
	unsigned char* pCharBits;
    int             t;
	pCharBits = g_pCharset + ((int)ubChar) * 8; 
	for ( t=0; t < 8; t++)
	{
		long  nPix8;
		nPix8 = ( long) pCharBits [ t];
		nPix8 = nPix8 << 24;
		PIXEL(0);
		PIXEL(1);
		PIXEL(2);
		PIXEL(3);
		PIXEL(4);
		PIXEL(5);
		PIXEL(6);
		PIXEL(7);
		pChunk += wModulo; 		
	}
}

void DrawChar32 ( unsigned char ubChar, long *pChunk, int wModulo, int wColor)
{
	unsigned char* pCharBits;
    int             t;
	pCharBits = g_pCharset + ((int)ubChar) * 8; 
	for ( t=0; t < 8; t++)
	{
		long  nPix8;
		nPix8 = ( long) pCharBits [ t];
		nPix8 = nPix8 << 24;
		PIXEL(0);
		PIXEL(1);
		PIXEL(2);
		PIXEL(3);
		PIXEL(4);
		PIXEL(5);
		PIXEL(6);
		PIXEL(7);
		pChunk += wModulo; 		
	}
}

void Draw_Char ( unsigned char ubChar, int pChunk, int wModulo, int wColor, int uwPixelSize)
{
    if ( g_wPrintMode == PRINTMODE_SHADOW)
    {
        if ( uwPixelSize == 16)
            DrawChar16 ( ubChar, (short *)(pChunk + 2 + wModulo*2), wModulo, 0);
        if ( uwPixelSize == 32)
            DrawChar32 ( ubChar, (long *)(pChunk + 4 + wModulo*4), wModulo, 0);
    }

    if ( uwPixelSize == 16)
        DrawChar16 ( ubChar, (short *)pChunk, wModulo, wColor);
    if ( uwPixelSize == 32)
        DrawChar32 ( ubChar, (long *)pChunk, wModulo, wColor);
}

// ---------------------------------------------------------------------------

void PrintAt ( char *psCadena, int iX, int iY, int nColor)
{
    unsigned int uwChunk;
    int wWidth, wHeight, wSize;

    wWidth  = GetScreenWidth ();
    wHeight = GetScreenHeight ();
    wSize   = GetScreenPixelSize ();
    uwChunk  = ( unsigned int)GetScreenPixels ();
    uwChunk += ( wSize * 8 * (iX + ( iY * wWidth)));
    if ( iY < (wHeight/8))
    {
        int iXc = 0;
        if ( wSize == 2)
        {
            short *pChunk = (short *) uwChunk; 
            while (( iX < (wWidth/8)) && ( psCadena[iXc] != 0))
            {
                Draw_Char ( psCadena[iXc], (int)pChunk, wWidth, nColor, 16);
                if ( psCadena[iXc] == '\n')
                {
                    g_wX = 0, iX = 0, g_wY++;
                    uwChunk = ( unsigned int)GetScreenPixels () + 
                              ( wSize * 8 * (iX + ( g_wY * wWidth)));
                    pChunk = (short *) uwChunk;
                }
                else
                {
                    iX++, pChunk += 8;
                    if ( iX >= (GetScreenWidth () >>3))
                    {
                        g_wX = 0, iX = 0, g_wY++;
                        uwChunk = ( unsigned int)GetScreenPixels () + 
                              ( wSize * 8 * (iX + ( g_wY * wWidth)));
                        pChunk = (long *) uwChunk;
                    }
                }
                iXc++;
            }
        }
        else
            if ( wSize == 4)
            {
                long *pChunk = (long *) uwChunk; 
                while (( iX < (wWidth/8)) && ( psCadena[iXc] != 0))
                {
                    Draw_Char ( psCadena[iXc], (int)pChunk, wWidth, nColor, 32);
                if ( psCadena[iXc] == '\n')
                {
                    g_wX = 0, iX = 0, g_wY++;
                    uwChunk = ( unsigned int)GetScreenPixels () + 
                              ( wSize * 8 * (iX + ( g_wY * wWidth)));
                    pChunk = (long *) uwChunk;
                }
                else
                {
                    iX++, pChunk += 8;
                    if ( iX >= (GetScreenWidth () >>3))
                    {
                        g_wX = 0, iX = 0, g_wY++;
                        uwChunk = ( unsigned int)GetScreenPixels () + 
                              ( wSize * 8 * (iX + ( g_wY * wWidth)));
                        pChunk = (long *) uwChunk;
                    }
                }
                iXc++;
            }                
        }
        else
            { assert(0);}
    }
    g_wX = iX;
}

